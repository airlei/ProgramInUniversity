    private String sale_nbr;
    private String cnt;
    private String round;

    public String getDay_id() {
        return day_id;
    }

    public void setDay_id(String day_id) {
        this.day_id = day_id;
    }

    public String getSale_nbr() {
        return sale_nbr;
    }


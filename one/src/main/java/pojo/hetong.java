package pojo;

public class hetong {
    private int id;
    private String nameHetong;
    private String nameHuowu;
    private String kind;
    private int color;
    private int num;
    private String timeQian;
    private String timeJiao;
    private int people;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNameHetong() {
        return nameHetong;
    }

    public void setNameHetong(String nameHetong) {
        this.nameHetong = nameHetong;
    }

    public String getNameHuowu() {
        return nameHuowu;
    }

    public void setNameHuowu(String nameHuowu) {
        this.nameHuowu = nameHuowu;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getTimeQian() {
        return timeQian;
    }

    public void setTimeQian(String timeQian) {
        this.timeQian = timeQian;
    }

    public String getTimeJiao() {
        return timeJiao;
    }

    public void setTimeJiao(String timeJiao) {
        this.timeJiao = timeJiao;
    }

    public int getPeople() {
        return people;
    }

    public void setPeople(int people) {
        this.people = people;
    }
}
